    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String dni = jTextField1.getText().toUpperCase();
        String contrasena = jTextField2.getText().toUpperCase();
        boolean login = Fachada.logearse(dni, contrasena);
        if (login){
            //si es un usuario correcto con su contrase침a, se mira si es admin o no
            if(Fachada.getAdminUser())
            {
                //si el usuario logeado es admin, se le abre la pesta침a de admin
                new InterfazAdmin().setVisible(true);
            }else{
                //si no es admin, la normal
                new InterfazUsuario().setVisible(true);
            }
            this.dispose();
        }else{
            //si se ha metido mal el user o la pass, se le notifica al usuario
            new UsuarioContrasenaError().setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

